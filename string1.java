import java.util.*;
import java.lang.*;
import java.io.*;
class Ideone
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner s = new Scanner (System.in);
		String st = s.next();
		int a = s.nextInt();
		for (int i=0;i<a;i++)
		{
		    char ch = st.charAt(i);
		    System.out.print(ch);
		}
	
	}
}
